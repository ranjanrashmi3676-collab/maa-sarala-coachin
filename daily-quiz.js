const humanEyeQuestions = [];

const electricityQuestions = [];

const magneticQuestions = [];

const environmentQuestions = [];

const energyQuestions = [];
const chapterQuestions = {
    reflection: reflectionQuestions,
    humanEye: humanEyeQuestions,
    electricity: electricityQuestions,
    magnetic: magneticQuestions,
    environment: environmentQuestions,
    energy: energyQuestions
};

let questions = [];
let currentQuestion = 0;
let score = 0;
let timeLeft = 1200;
let timer;

const question = document.getElementById("question");
const questionNumber = document.getElementById("questionNumber");
const answers = document.querySelectorAll(".answer");
const scoreText = document.getElementById("score");
const progressBar = document.getElementById("progressBar");
const timerText = document.getElementById("timer");

questions.sort(() => Math.random() - 0.5);
function loadChapter(chapter) {

    questions = [...chapterQuestions[chapter]];

    currentQuestion = 0;
    score = 0;
    timeLeft = 1200;

    clearInterval(timer);

    document.getElementById("score").innerHTML = "0";

    document.getElementById("timer").innerHTML = "20:00";

    document.querySelectorAll(".chapter-btn").forEach(btn=>{
        btn.classList.remove("selected");
    });

    event.target.classList.add("selected");

    loadQuestion();

    timer = setInterval(updateTimer,1000);

}
function loadQuestion() {
    const q = questions[currentQuestion];

    questionNumber.innerHTML = `Question ${currentQuestion + 1} / ${questions.length}`;
    question.innerHTML = q.question;

    answers.forEach((btn, index) => {
        btn.innerHTML = q.answers[index];
        btn.disabled = false;
        btn.style.background = "";

        btn.onclick = () => checkAnswer(index);
    });

    progressBar.style.width =
        ((currentQuestion + 1) / questions.length) * 100 + "%";
}

function checkAnswer(index) {

    answers.forEach(btn => btn.disabled = true);

    if (index === questions[currentQuestion].correct) {
        score++;
        scoreText.innerHTML = score;
        answers[index].style.background = "#00c853";
    } else {
        answers[index].style.background = "#f44336";
        answers[questions[currentQuestion].correct].style.background = "#00c853";
    }
}

document.getElementById("nextBtn").onclick = () => {

    currentQuestion++;

    if (currentQuestion < questions.length) {
        loadQuestion();
    } else {
        finishQuiz();
    }

};

document.getElementById("startBtn").onclick = () => {

    document.getElementById("startBtn").style.display = "none";
    document.getElementById("nextBtn").style.display = "inline-block";

    loadQuestion();

    timer = setInterval(updateTimer, 1000);

};

function updateTimer() {

    timeLeft--;

    const min = Math.floor(timeLeft / 60);
    const sec = timeLeft % 60;

    timerText.innerHTML =
        `${String(min).padStart(2, "0")}:${String(sec).padStart(2, "0")}`;

    if (timeLeft <= 0) {

        finishQuiz();

    }

}

function finishQuiz() {

    clearInterval(timer);

    document.querySelector(".glass").innerHTML = `
        <h1>🏆 Quiz Completed</h1>

        <h2>Your Score</h2>

        <h1>${score} / ${questions.length}</h1>

        <button onclick="location.reload()">
            Play Again
        </button>
    `;

}
  